package main.java.interview.loops;

public class WhileLoops {

    public static void main(String[] args) {

        int i = 0;

        while (i < 10) {
            System.out.println(i);
            i++;
        }

        String[] words = {"Banana", "Avocado"};
        int z = 0;

        while (z < 2) {
            System.out.println(words[z]);
            z++;
        }
    }
}
